package de.isuret.polos.AetherOnePi.processing2.elements;

public class ColorElement {

    public int r = 0;
    public int g = 0;
    public int b = 0;
    public float a = 0;

    public ColorElement(int r, int g, int b, float a) {
        this.r = r;
        this.g = g;
        this.b = b;
        this.a = a;
    }
}
